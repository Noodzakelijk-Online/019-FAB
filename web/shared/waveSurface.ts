export function getWaveSurfaceCatalog(): WaveSurfaceCatalog {
  return waveSurfaceCatalog;
}

export function findWaveSurface(surfaceId: string): WaveSurface | undefined {
  for (const module of waveSurfaceCatalog.modules) {
    const surface = module.surfaces.find((item) => item.id === surfaceId);
    if (surface) return surface;
  }
  return undefined;
}

export function resolveWaveSurfaceForDocument(documentType: string): WaveDocumentRoutingRule {
  const normalized = documentType.trim().toLowerCase();
  const catalog: WaveSurfaceCatalog = waveSurfaceCatalog;
  return (
    catalog.documentRoutingRules.find((rule) => rule.documentTypes.includes(normalized)) ||
    catalog.documentRoutingRules[0]
  );
}

export function getWaveActionsForSurface(surfaceId: string): WaveSurfaceAction[] {
  const catalog: WaveSurfaceCatalog = waveSurfaceCatalog;
  return catalog.actions.filter((action) => action.surfaceId === surfaceId);
}

export function findWaveAction(actionId: string): WaveSurfaceAction | undefined {
  const catalog: WaveSurfaceCatalog = waveSurfaceCatalog;
  return catalog.actions.find((action) => action.id === actionId);
}

export function getWaveParitySummary() {
  const catalog: WaveSurfaceCatalog = waveSurfaceCatalog;
  const surfaces = catalog.modules.flatMap((module) => module.surfaces);
  const observedControls = catalog.featureInventory.flatMap((page) => page.controls);
  const actionsBySafety = catalog.actions.reduce<Record<WaveActionSafety, number>>(
    (acc, action) => {
      acc[action.safety] += 1;
      return acc;
    },
    {
      read_only: 0,
      safe_draft: 0,
      requires_confirmation: 0,
      requires_credentials: 0,
      unsupported: 0,
    }
  );

  return {
    modules: catalog.modules.length,
    surfaces: surfaces.length,
    menuGroups: catalog.menuInventory.length,
    menuItems: catalog.menuInventory.reduce((count, group) => count + group.items.length, 0),
    syncContracts: catalog.syncContracts.length,
    featurePages: catalog.featureInventory.length,
    observedControls: observedControls.length,
    actions: catalog.actions.length,
    legacySurfaces: surfaces.filter((surface) => surface.legacyAccounting).length,
    actionsBySafety,
    pagesByAutomationMode: catalog.featureInventory.reduce<Record<WaveAutomationMode, number>>(
      (acc, page) => {
        acc[page.automationMode] += 1;
        return acc;
      },
      {
        observe: 0,
        safe_draft: 0,
        confirmed_execute: 0,
        credential_owner: 0,
        blocked: 0,
      }
    ),
  };
}
