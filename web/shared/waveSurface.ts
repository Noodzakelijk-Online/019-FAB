export type WaveSurfaceModule = {
  id: WaveSurfaceModuleId;
  label: string;
  surfaces: WaveSurface[];
};

export type WaveAccountFamily = {
  type: "asset" | "liability" | "equity" | "income" | "expense";
  label: string;
  examples: string[];
  fabMapping: string;
};

export type WaveDocumentRoutingRule = {
  documentTypes: string[];
  targetSurfaceId: string;
  fallbackSurfaceId: string;
  reason: string;
};

export type WaveActionMode = "read" | "write" | "export" | "connect" | "admin";

export type WaveActionSafety =
  | "read_only"
  | "safe_draft"
  | "requires_confirmation"
  | "requires_credentials"
  | "unsupported";

export type WaveSurfaceAction = {
  id: string;
  surfaceId: string;
  label: string;
  mode: WaveActionMode;
  safety: WaveActionSafety;
  requiredFields: string[];
  optionalFields: string[];
  result: string;
  workflowNotes: string;
};

export type WaveAutomationMode =
  | "observe"
  | "safe_draft"
  | "confirmed_execute"
  | "credential_owner"
  | "blocked";

export type WaveObservedControl = {
  label: string;
  kind: "page" | "link" | "button" | "input" | "menu" | "filter" | "report" | "external" | "tab" | "dialog";
  safety: WaveActionSafety;
  fabActionId?: string;
  notes: string;
};

